class Product {
  String productName;
  String productCategory;
  double length;
  double width;
  double breadth;
  double costPrice;
  double marketPrice;

  Product({
    required this.productName,
    required this.productCategory,
    required this.length,
    required this.width,
    required this.breadth,
    required this.costPrice,
    required this.marketPrice,
  });
}

